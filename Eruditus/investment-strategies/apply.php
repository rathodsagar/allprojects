<section class="participant-profile  common-pad pt-0">
 <div class="container">
  <span class="anchor" id="session-topics"></span>
  <div class="row">
   <div class="col-md-12 col-sm-12">
    <h2 class="main-title pb-3">Syllabus</h2>
    <p class="pb-4">This program moves from an introduction of the fundamentals of investing to an overview of capital markets, then quickly to the mechanisms of active management and advanced analytical theory for stock selection. Participants will apply their knowledge to analyze performance factors for their own stock picks. </p>
    <p class="pb-4">Featuring two 90-minute webinars per week, the program includes pre-readings, polls, moderated discussion boards, and crowdsource activities.</p>
    <div class="rep-com">
     <h4>Session titles:</h4>
     <dl id="module-list">
      <dt>Markets Introduction</dt>
      <dd>Learn the fundamentals of the stock market, including the different marketable securities, the trading process, and how liabilities can be a powerful tool or a tremendous risk in your portfolio.</dd>

      <dt>Risk and Investing</dt>
      <dd>Learn the fundamentals of measuring risk in the stock market, including standard deviation, the Sharpe Ratio, and correlation. You will be able to discuss these concepts from a historical perspective and begin to understand the considerations of using these strategies in your portfolio.</dd>

      <dt>Market Risk Premium Part I</dt>
      <dd>Examine the concept of a cheap stock versus an expensive stock and how that relates to expected and excess returns. You will also learn how to calculate the dividend yield and use the price-to-earnings ratio and Shiller’s CAPE as a method to evaluate stocks.</dd>

      <dt>Patterns in Stock Returns Part I</dt>
     <dd> Examine how expected excess returns are theoretically impacted by the cyclically-adjusted price-to-earnings ratio and whether or not that differs from reality. You also take a deeper look at how the size factor and value investing strategy are applicable in today’s market.</dd>


      <dt>Major Asset Classes</dt>
      <dd>Walk away with an understanding of what the major asset classes are, including ETFs and where and how to view the data necessary to help you review the historical performance of markets around the globe.</dd>

     <dt>Active Management</dt>
      <dd>You will be introduced to the Capital Asset Pricing Model (or CAPM), the dividend discount model, and the often debated question of what it means for a market to be efficient. These topics will give you greater insight into how to value a security.</dd>

      <dt>Market Risk Premium Part II</dt>
      <dd>Delve into the cyclically-adjusted price-to-earnings ratio and how it relates to forecasting average and excess returns. You also learn about the caveats of using the price-to-earnings ratio for forecasting the return of individual stocks and evaluate the reliability of these strategies to forecast returns.</dd>

      <dt>Patterns in Stock Returns Part II</dt>
     <dd>Build on the previous session and examine momentum and profitability as strategies for stock selections. You will leave this session knowing how to use the value effect, size effect, momentum, and profitability factor to make informed stock selections.</dd>
      
    </dl>

   </div>
